// Helper function to format airport for display
export const formatAirport = (airport) => {
  return `${airport.city}, ${airport.state} (${airport.code})`;
};

// Helper function to search airports
export const searchAirports = (query) => {
  if (!query) return [];

  const lowerQuery = query.toLowerCase();
  return US_AIRPORTS.filter(airport =>
    airport.city.toLowerCase().includes(lowerQuery) ||
    airport.code.toLowerCase().includes(lowerQuery) ||
    airport.state.toLowerCase().includes(lowerQuery) ||
    airport.name.toLowerCase().includes(lowerQuery)
  ).slice(0, 10); // Limit to 10 results
};
